package contadores;
public class Contadores {
	public static void main(String[] args) {
		int c = 3;
		System.out.println("C = " + ++c);
		System.out.println("C = " + c++);
		System.out.println("C = " + c);
		System.out.print("\n");
		System.out.println("C = " + --c);
		System.out.println("C = " + c--);
		System.out.println("C = " + c);
		System.out.println("C = " + c);
	}
}
